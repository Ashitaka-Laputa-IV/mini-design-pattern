/*
Q1: C++ 中 NVI 惯用法和模板方法模式的关系？
    NVI 是 C++ 特有的模板方法实现方式。
    它通过"非虚公有接口 + 私有虚函数"的组合，
    实现了对算法骨架的严格控制。

Q2: 为什么基类的模板方法要设为非虚（Non-Virtual）？
    因为算法骨架是"固定"的，不应该被子类改变。
    如果设为 virtual，子类可能不小心重写掉整个骨架，
    或者只重写一部分而忘记调用基类方法。

Q3: 为什么虚函数要设为 private？
    NVI 的核心设计：
      - 公有接口：非虚的模板方法，提供"合同"
      - 私有实现：虚函数，提供"可定制点"
    这样用户只能看到模板方法，不能直接调用具体的步骤。

Q4: C++ 允许派生类覆盖基类的 private 虚函数吗？
    允许！这是 C++ 特有的规则。
    访问权限（public/private）和覆盖权限是两回事。
    派生类可以覆盖基类的 private 虚函数，但外部不能调用。
    这看起来违反直觉，但正是 NVI 的基础。

Q5: C++11 的 final 关键字和 NVI 的关系？
    C++11 引入了 final 关键字，可以直接标记一个函数为"不可重写"：
      void prepareRecipe() final { ... }
    这样即使方法是 virtual，子类也不能重写它。
    NVI 是 C++11 之前的惯用法，现在可以和 final 结合使用。

Q6: 模板方法模式和策略模式在 C++ 中的区别？
    模板方法：
      - 编译时绑定（继承，静态多态）
      - 通过虚函数实现
      - 代码结构清晰，但运行时开销（虚函数调用）
    
    策略模式：
      - 运行时绑定（组合，动态多态）
      - 通过函数指针 / std::function / 接口类实现
      - 更灵活，但代码结构更复杂
*/
