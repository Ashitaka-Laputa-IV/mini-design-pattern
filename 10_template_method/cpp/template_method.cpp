/*
 * 💡 NVI 的正确用法：基类模板方法中加入前置/后置处理
 *
 * 在基类 CaffeineBeverage 中，如果我们想加入日志功能：
 *
 *   class CaffeineBeverage {
 *   public:
 *       void prepareRecipe() {
 *           log("开始冲泡");
 *           boilWater();
 *           brew();
 *           pourInCup();
 *           if (customerWantsCondiments()) {
 *               addCondiments();
 *           }
 *           log("冲泡完成");
 *       }
 *   private:
 *       void log(const std::string& msg) { ... }
 *       virtual void brew() = 0;
 *       ...
 *   };
 *
 * 所有子类都会自动获得日志功能，不需要修改子类任何代码！
 * 这就是 NVI 的强大之处。
 */

// ============================================================================
// 展示 NVI 的访问控制效果
// ============================================================================

void demonstrate_access_control() {
    std::cout << "\n========================================\n";
    std::cout << "📌 NVI 访问控制演示\n";
    std::cout << "========================================\n";
    std::cout << "  通过 NVI，外部用户只能看到:\n";
    std::cout << "    tea.prepareRecipe();      // ✅ 公有接口\n";
    std::cout << "    tea.boilWater();          // ❌ 不可访问（非虚但私有... 实际是protected）\n";
    std::cout << "    tea.brew();               // ❌ 不可访问（私有纯虚函数）\n";
    std::cout << "\n  这就是 NVI 的设计意图：\n";
    std::cout << "    用户只和公有接口交互，不需要知道内部实现细节。\n";
}


