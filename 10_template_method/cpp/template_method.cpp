/*
================================================================================
Lesson 10: Template Method — C++ 模板方法模式（实现 + 测试）
================================================================================
  本文件演示 C++ 中 NVI 惯用法的具体使用。
  NVI = Non-Virtual Interface（非虚接口）

  🎯 NVI 惯用法的优势：
    1. 在基类的公有接口中，可以加入"前置条件检查"和"后置处理"
    2. 精确控制子类的重写范围（private/protected）
    3. 基类的模板方法是 final（非虚），子类不能破坏算法骨架

  对比 Java/C# 的 final 关键字：
    C++ 没有语言层面的"禁止重写"关键字（C++11 后可用 final 关键字），
    NVI 通过"非虚 + 私有虚函数"的组合达到了同样的效果。
================================================================================
*/

