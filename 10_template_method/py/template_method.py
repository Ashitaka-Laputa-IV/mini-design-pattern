"""
================================================================================
Lesson 10: Template Method — 模板方法模式（模式实现）
================================================================================
  上节课的 bad_template_method.py 我们看到了 CTRL+C/V 式继承的灾难，
  现在来看看设计模式怎么优雅解决。

📐 设计模式定义：
   模板方法模式(Template Method)定义一个操作中的算法骨架，
   而将一些步骤延迟到子类中实现。
   模板方法使得子类可以不改变一个算法的结构即可重定义该算法的某些特定步骤。

🎯 解决的问题：
   - 避免公共代码重复（DRY 原则）
   - 固定算法结构，防止子类破坏流程
   - 让扩展新功能变得简单（开闭原则）

🏗 结构：
   ┌──────────────────────────────┐
   │  AbstractClass (抽象基类)      │
   ├──────────────────────────────┤
   │  + template_method()   ← 骨架 │
   │  + step1()             ← 公共 │
   │  + step2()             ← 公共 │
   │  + abstract step3()    ← 抽象 │
   │  + hook()              ← 钩子 │
   └──────────────────────────────┘
              △
              │
   ┌──────────────────────────────┐
   │  ConcreteClass (具体子类)     │
   ├──────────────────────────────┤
   │  + step3()             实现   │
   │  + hook()              可选   │
   └──────────────────────────────┘
"""

# ============================================================================
# 抽象基类：定义"冲泡饮料"的算法骨架
# ============================================================================

from abc import ABC, abstractmethod


class CaffeineBeverage(ABC):
    """
    含咖啡因饮料——抽象基类

    这就是模板方法模式中的 AbstractClass。
    它定义了一个"冲泡饮料"的通用算法骨架。

    💡 关键设计决策：
      - 模板方法（prepare_recipe）是"骨架"，不允许子类重写
      - 公共步骤（boil_water, pour_in_cup）在基类中实现
      - 可变步骤（brew, add_condiments）定义为抽象方法
      - 钩子方法（customer_wants_condiments）提供可选的控制点
    """

    # ========================================================================
    # 模板方法：定义算法的骨架
    # ========================================================================

    def prepare_recipe(self):
        """
        🎯 模板方法（Template Method）

        定义"冲泡饮料"的算法骨架，固定了步骤顺序：
          1. 烧水
          2. 冲泡
          3. 倒入杯子
          4. 加调料

        这个方法不应该被子类重写（Python 没有语言级别的 final，
        但约定：子类不要重写此方法）。

        🔑 这就是"好莱坞原则"的体现：
           "Don't call us, we'll call you."
           基类控制流程，子类只提供实现细节。
        """
        self.boil_water()
        self.brew()                  # 抽象方法→子类实现
        self.pour_in_cup()
        if self.customer_wants_condiments():  # 钩子方法
            self.add_condiments()    # 抽象方法→子类实现

    # ========================================================================
    # 公共步骤：在基类中实现，所有子类共享
    # ========================================================================

    def boil_water(self):
        """烧水——所有饮料都一样，在基类中实现"""
        print("  1. 把水烧开")

    def pour_in_cup(self):
        """倒入杯子——所有饮料都一样"""
        print("  3. 把饮料倒入杯子")

    # ========================================================================
    # 抽象方法：子类必须实现
    # ========================================================================

    @abstractmethod
    def brew(self):
        """冲泡——茶和咖啡不同，子类实现"""
        pass

    @abstractmethod
    def add_condiments(self):
        """加调料——茶和咖啡不同，子类实现"""
        pass

    # ========================================================================
    # 钩子方法（Hook）：子类可选择性地重写
    # ========================================================================

    def customer_wants_condiments(self):
        """
        🪝 钩子方法（Hook）

        钩子的作用：
          - 让子类有能力"介入"算法的某个节点
          - 默认返回 True（默认加调料）
          - 子类可以覆盖此方法来改变行为

        本例中，顾客可以选择是否加调料。
        实际应用中钩子可以用来：
          - 控制流程分支
          - 提供扩展点
          - 让子类决定是否执行某个步骤
        """
        return True


# ============================================================================
# 具体子类：茶
# ============================================================================

class Tea(CaffeineBeverage):
    """
    茶——继承自 CaffeineBeverage

    只需要实现茶特有的步骤：
      - brew(): 用茶叶冲泡
      - add_condiments(): 加柠檬
    其他步骤（烧水、倒杯）直接继承。
    """

    def brew(self):
        """茶的冲泡方式：浸泡茶叶"""
        print("  2. 把茶叶放入水中浸泡 3 分钟")

    def add_condiments(self):
        """茶的调料：加柠檬"""
        print("  4. 加柠檬片")


# ============================================================================
# 具体子类：咖啡
# ============================================================================

class Coffee(CaffeineBeverage):
    """
    咖啡——继承自 CaffeineBeverage

    只需要实现咖啡特有的步骤：
      - brew(): 用咖啡粉冲泡
      - add_condiments(): 加糖和奶
    """

    def brew(self):
        """咖啡的冲泡方式：冲泡咖啡粉"""
        print("  2. 用热水冲泡咖啡粉")

    def add_condiments(self):
        """咖啡的调料：加糖和牛奶"""
        print("  4. 加糖和牛奶")


# ============================================================================
# 另一个具体子类：展示了钩子方法的用法
# ============================================================================

class BlackTea(CaffeineBeverage):
    """
    清茶/黑茶——不想要任何调料的顾客

    展示了钩子方法的用途：
    通过重写 customer_wants_condiments() 返回 False，
    跳过了"加调料"步骤。
    """

    def brew(self):
        print("  2. 把茶叶放入水中浸泡 2 分钟")

    def add_condiments(self):
        # 清茶不加任何调料
        print("  4. （不加任何调料）")

    def customer_wants_condiments(self):
        """
        重写钩子方法：顾客不想加调料

        💡 这里可以加入用户交互逻辑，比如：
          answer = input("要不要加调料？(y/n) ")
          return answer.lower() == 'y'
        """
        print("  4. （顾客选择了不加调料，跳过此步骤）")
        return False


# ============================================================================
# 扩展示例：增加热巧克力（展示扩展的便捷性）
# ============================================================================

class HotChocolate(CaffeineBeverage):
    """
    热巧克力——新增一种饮料

    只需要 3 行代码！不需要重复烧水、倒杯的逻辑。

    这就是"开闭原则"的体现：
      - 对扩展开放：可以轻松增加新饮料
      - 对修改关闭：不需要修改基类 CaffeineBeverage
    """

    def brew(self):
        print("  2. 把巧克力粉加入热水中搅拌")

    def add_condiments(self):
        print("  4. 加棉花糖和奶油")


