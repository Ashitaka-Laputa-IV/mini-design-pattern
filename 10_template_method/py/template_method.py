"""
Q1: 好莱坞原则（Hollywood Principle）是什么？和模板方法模式的关系？
    "Don't call us, we'll call you."
    底层组件不要主动调用高层组件，而是由高层组件来调用底层组件。
    在模板方法模式中，基类的模板方法（高层）调用子类的方法（底层），
    子类不需要主动调用基类——基类"打电话给你"。

    对比：传统做法是子类调用基类（super()），
    模板方法是基类调用子类（模板方法中调用抽象方法）。
    这就是"控制反转"（IoC）的一种体现。

Q2: 模板方法模式和策略模式的区别？
    模板方法模式：
      - 在基类层面定义算法骨架，子类通过继承来改变部分步骤
      - 是"继承"的运用
      - 适合算法结构固定、变化的是具体步骤的场景

    策略模式：
      - 用组合的方式，把算法封装成独立的对象
      - 是"组合"的运用
      - 适合算法可以整体替换的场景

    口诀：模板方法=继承+固定骨架，策略模式=组合+可替换算法

Q3: 什么是 NVI（Non-Virtual Interface）惯用法？
    C++ 中的 NVI 惯用法：
      - 公有接口是非虚函数（Non-Virtual）
      - 虚函数设为 private 或 protected
      - 用户通过公有接口调用，接口内部调用虚函数

    好处：
      - 在基类接口中可以加入"前置/后置"处理逻辑
      - 控制子类可以重写哪些方法（不是所有方法都开放）
      - 分离"接口"和"实现"

Q4: 模板方法中的"钩子方法"有什么用？
    钩子方法允许子类"插入"到算法的特定位置：
      - 控制流程：决定某一步是否执行（如 customer_wants_condiments）
      - 提供通知：在算法执行前后通知子类（如 on_start, on_finish）
      - 提供默认实现：子类可以选择不重写

Q5: 模板方法模式的缺点？
    - 继承的固有缺点：子类和父类紧密耦合
    - 算法骨架固定后难以改变（违背开闭原则的"修改"部分）
    - 子类越多，维护越复杂（每个子类都要关注骨架是否变化）

💡 面试官变种题：Spring 框架中的 JdbcTemplate 就是模板方法模式的应用。
    它固定了"获取连接→执行SQL→处理结果→关闭连接"的骨架，
    开发者只需要提供 SQL 和结果处理器（RowMapper）。
"""
