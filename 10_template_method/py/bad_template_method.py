# 反模式：CTRL+C/V 式继承
# ============================================================================

class Tea:
    """
    茶类——冲泡茶叶

    问题：Tea 和 Coffee 大量重复代码。
    """

    def prepare_recipe(self):
        """冲泡茶的完整流程"""
        self.boil_water()
        self.steep_tea_bag()       # ⚠ 茶特有的：浸泡茶叶
        self.pour_in_cup()
        self.add_lemon()           # ⚠ 茶特有的：加柠檬

    def boil_water(self):
        """烧水"""
        print("  1. 把水烧开")

    def steep_tea_bag(self):
        """用茶叶冲泡"""
        print("  2. 把茶叶放入水中浸泡 3 分钟")

    def pour_in_cup(self):
        """倒入杯子"""
        print("  3. 把饮料倒入杯子")

    def add_lemon(self):
        """加柠檬"""
        print("  4. 加柠檬片")


class Coffee:
    """
    咖啡类——冲泡咖啡

    😱 和 Tea 的代码几乎一样！只有第2步和第4步不同！
    """

    def prepare_recipe(self):
        """冲泡咖啡的完整流程"""
        self.boil_water()
        self.brew_coffee_grounds()  # ⚠ 咖啡特有的：冲泡咖啡粉
        self.pour_in_cup()
        self.add_sugar_and_milk()   # ⚠ 咖啡特有的：加糖和奶

    def boil_water(self):
        """烧水"""
        print("  1. 把水烧开")   # 🔴 和 Tea 完全一样！

    def brew_coffee_grounds(self):
        """用咖啡粉冲泡"""
        print("  2. 用热水冲泡咖啡粉")

    def pour_in_cup(self):
        """倒入杯子"""
        print("  3. 把饮料倒入杯子")  # 🔴 和 Tea 完全一样！

    def add_sugar_and_milk(self):
        """加糖和奶"""
        print("  4. 加糖和牛奶")


"""
❌ 这个方案的问题：

  问题 1：代码重复
    boil_water() 和 pour_in_cup() 在两个类中一模一样。
    同样的代码出现了两次——DRY (Don't Repeat Yourself) 原则被彻底违反。

  问题 2：维护困难
    如果需求变化：烧水步骤改为"将水加热到85度"。
    你需要改 Tea 和 Coffee 两个类。改了一个忘了改另一个？Bug 来了。

  问题 3：扩展性差
    如果增加新饮料：热巧克力、奶茶、花茶……
    每加一个就要复制一遍模板代码，工作量 O(n)。

  问题 4：算法结构不统一
    prepare_recipe() 在每个类中独立实现。
    虽然目前步骤顺序一致，但无法保证未来保持一致。
    这就像让每个人自己决定"做菜步骤"——有人可能先放油再热锅，有人先热锅再放油。

  💡 本质问题：公共算法骨架和具体实现步骤耦合在一起了。
    我们需要一种方式：
      - 把"算法骨架"固定下来（烧水→冲泡→倒杯→加料）
      - 把"可变步骤"交给子类去实现（冲泡什么？加什么料？）
    这就是——模板方法模式（Template Method）！
"""


# ============================================================================
# 🤔 如果你来设计，你会怎么做？
#
# 你可能会想到"继承"：
#   写一个基类，把公共步骤放在基类里，不同的步骤让子类自己实现。
# 这就是模板方法模式的雏形！
#
# 模板方法模式的核心思想：
#   1. 在基类中定义一个算法的"骨架"（模板方法）
#   2. 把一些步骤延迟到子类中实现
#   3. 子类可以重定义算法的某些步骤，但不能改变算法结构
#
# 就像你去餐厅点"定食"（套餐）：
#   - 骨架：先上前菜 → 再上主菜 → 最后上甜点（算法结构固定）
#   - 可变：前菜是沙拉还是刺身？主菜是牛排还是鱼？（子类决定细节）
# ============================================================================

if __name__ == "__main__":
    print("=" * 50)
    print("☕ 冲泡茶")
    print("=" * 50)
    tea = Tea()
    tea.prepare_recipe()

    print("\n" + "=" * 50)
    print("☕ 冲泡咖啡")
    print("=" * 50)
    coffee = Coffee()
    coffee.prepare_recipe()

