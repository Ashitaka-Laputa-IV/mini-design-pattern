# ============================================================================
# 反模式：全局变量 + 手动管理
# ============================================================================

_config_instance = None  # 全局变量


def get_config():
    """用全局变量 + 函数来模拟单例——但问题很多"""
    global _config_instance
    if _config_instance is None:
        _config_instance = {"db_host": "localhost", "db_port": 3306}
        print("  [警告] 配置文件已加载")  # debug
    return _config_instance


"""
❌ 这个方案的问题：
  1. 全局变量可以被任意修改：_config_instance = None 就废了
  2. 模块间依赖隐式全局状态，测试困难
  3. 没有封装：使用者需要知道"要用 get_config() 而不是直接 new"
  4. 多人协作时，有人可能 new ConfigManager() 走另一条路
"""


# ============================================================================
# 🤔 重新思考：什么样的类才是"单例"？
#
# 本质需求：一个类最多只有一个实例，且全局可访问。
#
# 用代码表达这个约束：
#   1. 控制实例的创建（不能随意 new/__init__）
#   2. 保存唯一的实例
#   3. 提供全局访问点
# ============================================================================

if __name__ == "__main__":
    # 先看错误做法
    c1 = get_config()
    c2 = get_config()
    print(f"c1 is c2? {c1 is c2}")  # True，但依赖全局变量

    # 如果其他人不小心这样写：
    c3 = {"db_host": "evil.com"}  # 直接创建新字典，绕过 get_config
    print(f"[反模式] 配置被篡改风险: {c1 == c3}")  # 没人能阻止
